static Suite *
cavsvideoparser_suite (void)
{
  Suite *s = suite_create ("CAVS video parser library");
  TCase *tc_chain = tcase_create ("general");

  suite_add_tcase (s, tc_chain);
  tcase_add_test (tc_chain, test_cavs_video_parser_identify_unit);
  tcase_add_test (tc_chain, test_cavs_video_parser_parse_sequence_header);
  tcase_add_test (tc_chain, test_cavs_video_parser_parse_i_picture);
  tcase_add_test (tc_chain, test_cavs_video_parser_parse_pb_picture);
  tcase_add_test (tc_chain,
      test_cavs_video_parser_parse_sequence_display_extension);
  tcase_add_test (tc_chain, test_cavs_video_parser_parse_copyright_extension);
  tcase_add_test (tc_chain,
      test_cavs_video_parser_parse_camera_parameters_extension);
  tcase_add_test (tc_chain, test_cavs_video_parser_parse_slice_i);
  tcase_add_test (tc_chain, test_cavs_video_parser_parse_slice_p);

  return s;
}

int
main (int argc, char **argv)
{
  int nf;
  Suite *s = cavsvideoparser_suite ();
  SRunner *sr = srunner_create (s);

  gst_check_init (&argc, &argv);

  srunner_run_all (sr, CK_NORMAL);
  nf = srunner_ntests_failed (sr);
  srunner_free (sr);

  return nf;
}
